// returns the value of 2 numbers added together

function addition(a, b) {
    return a + b;
}

// returns the value of 2 numbers subtracted from one another

function subtraction(a, b) {
    return a - b;
}

// returns the value of 2 numbers multiplied together

function multiplication(a, b) {
    return a * b;
}

// returns the value of 2 numbers divided from one another

function division(a, b) {
    return a / b;
}

// returns the modulus/remainder of 2 numbers

function modulus(a, b) {
    return a % b;
}